#include "../includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *len;

	len = NULL;
	while (*s)
	{
		if (*s == (char)c)
			len = (char*)s;
		s++;
	}
	if (!*s && c == '\0')
		len = (char*)s;
	return (len);
}
