#include "../includes/libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *copy;

	copy = *alst;
	while (copy && del)
	{
		copy = copy->next;
		ft_lstdelone(alst, del);
		*alst = copy;
	}
}
