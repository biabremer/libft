char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	char *s11;
	char *s22;

	s11 = s1;
	s22 = (char*)s2;
	while (*s11 != '\0')
		s11++;
	while (*s22 != '\0')
	{
		*s11 = *s22;
		s11++;
		s22++;
	}
	*s11 = '\0';
	return (s1);
}
